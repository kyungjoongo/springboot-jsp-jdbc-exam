package com.sample.jqgrid;

public class Fruit {
	
	
	
	public Fruit(int id, String name) {
		super();
		Id = id;
		Name = name;
	}

	public int Id;
	
	public String Name;

	public int getId() {
		return Id;
	}

	public void setId(int id) {
		Id = id;
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}
	
	
	
	
	
	
}